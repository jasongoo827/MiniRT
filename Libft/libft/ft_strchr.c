char	*ft_strchr(const char *s, int c)
{
	int		i;
	int		len;

	i = 0;
	len = 0;
	while (s[len] != '\0')
		len++;
	len += 1;
	while (i < len)
	{
		if (((char)*(s + i)) == (char)c)
			return ((char *)(s + i));
		i++;
	}
	return (0);
}

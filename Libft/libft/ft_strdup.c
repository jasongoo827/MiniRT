#include "libft.h"

char	*ft_strdup(const char *s1)
{
	size_t		len;
	int			i;
	char		*ret;

	len = ft_strlen(s1);
	i = 0;
	ret = (char *)malloc(sizeof(char) * (len + 1));
	if (!ret)
		return (0);
	while (s1[i] != '\0')
	{
		ret[i] = s1[i];
		i++;
	}
	ret[i] = '\0';
	return (ret);
}
